export const decideVerticalGridLayout = (l: number) =>
  l <= 0
    ? []
    : l === 1
      ? [1]
      : l === 2
        ? [1, 1]
        : l === 3
          ? [1, 2]
          : l === 4
            ? [1, 1, 2]
            : l === 5
              ? [1, 2, 2]
              : l === 6
                ? [1, 2, 3]
                : l === 7
                  ? [1, 3, 3]
                  : l === 8
                    ? [1, 2, 2, 3]
                    : l === 9
                      ? [1, 2, 3, 3]
                      : [1, 3, 3, 3];

